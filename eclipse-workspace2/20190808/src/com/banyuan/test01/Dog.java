package com.banyuan.test01;

public class Dog {
	private String name;
	private int age;
	private String sex;

	public void setName(String n) {
		name = n;
	}

	public void setAge(int a) {
		age = a;
	}

	public void setSex(String s) {
		sex = s;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public String getSex() {
		return sex;
	}
}
